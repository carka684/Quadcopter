function startButton_Callback(hObject, eventdata, handles)
% hObject    handle to startButton (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
axes(handles.axes1);
delete(instrfindall)
s = serial('COM11','BaudRate',115200);
fopen(s)
% Set axes
time = 0;
voltage = 0;
axes(handles.axes1);
plotVec = zeros(1,2);
for k = 1:2
    plotVec(k) = plot(eval(['handles.axes' int2str(k)]),time,voltage);
end

% plotHandle = plot(time,voltage);
% ylim(handles.axes1,[0 1023]);
% hold on;
% plotHandle2 = plot(handles.axes2,time,voltage);
% ylim(handles.axes2,[0 1023]);
% hold on;

numberOfValue = 1000;
count = 1;
valueVec = zeros(2,numberOfValue);
temp = 0;
tempSum = 0;
offset =  [-0.0037 -0.0006 -0.0139 -0.1291 0.0453 0.0144]';
while 1
    try
        temp = fscanf(s,'%f');
        
    end
    if isnumeric(temp) && length(temp) == 2
        %temp = temp + valueVec(:,end);
        valueVec = fliplr(circshift(fliplr(valueVec),1,2));
        valueVec(:,end) = temp;
        hold on;
        for k = 1:2
             set(plotVec(k),'YData',valueVec(k,:),'XData',1:numberOfValue);
        end

        drawnow;
    end 
end   
tempSum/count

disp('closing')
fclose(s)
delete(s)
clear s
